--  DDL for Trigger AUDIT_REPAIR_TRIGGER
--------------------------------------------------------

  CREATE OR REPLACE EDITIONABLE TRIGGER "OSAMA_MOBILE_DB"."AUDIT_REPAIR_TRIGGER" 
AFTER INSERT OR UPDATE OR DELETE
ON Repair_Requests
FOR EACH ROW
DECLARE
    v_operation VARCHAR2(10);
BEGIN
    IF INSERTING THEN
        v_operation := 'INSERT';
    ELSIF UPDATING THEN
        v_operation := 'UPDATE';
    ELSIF DELETING THEN
        v_operation := 'DELETE';
    END IF;

    INSERT INTO Audit_Log (
        Audit_ID,
        Table_Name,
        Operation_Type,
        User_Name,
        Operation_Date
    )
    VALUES (
        Audit_Log_SEQ.NEXTVAL,
        'Repair_Requests',
        v_operation,
        USER,
        SYSDATE
    );
END;

/
